int     ft_str_is_numeric(char *str)
{
    int cpt;

    cpt = 0;
	if (str[cpt] == '\0')
		return (1);
    while (str[cpt] != '\0')
    {
        if (str[cpt] <= '0' || str[cpt] >= '9')
            return (0);
        cpt ++;
    }
    return (1);
}
