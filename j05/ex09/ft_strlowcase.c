char    *ft_strlowcase(char *str)
{
    int cpt;

    cpt = 0;
    while (str[cpt] != '\0')
    {
        if ('A' <= str[cpt] && str[cpt] <= 'Z')
        {
            str[cpt] = str[cpt] + 32;
        }
        cpt++;
    }

    return str;
}
